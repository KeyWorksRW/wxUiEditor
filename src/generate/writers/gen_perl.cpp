PerlCodeGenerator::PerlCodeGenerator(Node* form_node) : BaseCodeGenerator(GEN_LANG_PERL, form_node)
{
}

void PerlCodeGenerator::GenerateClass(GenLang language, PANEL_PAGE panel_type,
                                      wxProgressDialog* /* progress */)
{
    m_language = language;
    m_panel_type = panel_type;
    ASSERT(m_language == GEN_LANG_PERL);
    Code code(m_form_node, m_language);

    if (m_header)
    {
        m_header->Clear();
    }
    m_source->Clear();
    m_source->SetTabToSpaces(GetLanguageTraits(m_language)->indent_size);
    m_source->SetLastLineBlank();

    // File header comment
    m_source->writeLine("# This file was generated by wxUiEditor. Do not edit.");
    m_source->writeLine();

    if (m_form_node->is_Gen(gen_Images))
    {
        return;
    }

    // Perl pragmas and module imports
    m_source->writeLine("use strict;");
    m_source->writeLine("use warnings;");
    m_source->writeLine();

    wxue::string class_name(m_form_node->as_string(prop_class_name));

    // Package declaration
    m_source->writeLine(wxue::string("package ") << class_name << ";");
    m_source->writeLine();
    m_source->writeLine("use kwxPerl::Controls;");
    m_source->writeLine("use kwxPerl::Constants;");
    m_source->writeLine();

    // Constructor
    m_source->writeLine("sub new {");
    m_source->Indent();
    m_source->writeLine("my ($class, $parent) = @_;");
    m_source->writeLine("my $self = bless {}, $class;");
    m_source->writeLine();

    GenerateConstructionCode(code);

    m_source->writeLine();
    m_source->writeLine("return $self;");
    m_source->Unindent();
    m_source->writeLine("}");
    m_source->writeLine();
    m_source->writeLine("1;");

    // Reset indentation
    m_source->ResetIndent();
}

auto PerlCodeGenerator::GenerateConstructionCode(Code& code) -> void
{
    auto* generator = m_form_node->get_NodeDeclaration()->get_Generator();
    code.clear();
    if (generator->ConstructionCode(code))
    {
        m_source->writeLine(code);
        m_source->writeLine();
    }

    code.clear();
    if (generator->SettingsCode(code))
    {
        if (code.size())
        {
            m_source->writeLine(code);
            m_source->writeLine();
        }
    }

    if (m_form_node->get_PropPtr(prop_window_extra_style))
    {
        code.clear();
        code.GenWindowSettings();
        if (code.size())
        {
            m_source->writeLine(code);
        }
    }

    m_source->SetLastLineBlank();
    for (const auto& child: m_form_node->get_ChildNodePtrs())
    {
        if (child->is_Gen(gen_wxContextMenuEvent))
        {
            continue;
        }
        GenConstruction(child.get());
    }

    code.clear();
    if (generator->AfterChildrenCode(code))
    {
        if (code.size())
        {
            m_source->writeLine();
            m_source->writeLine(code);
        }
    }
}
